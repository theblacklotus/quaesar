uint8_t* read_file_to_memory(const char* filename, size_t* size) {
    // Open the file
    FILE* file = fopen(filename, "rb");
    if (file == NULL) {
        perror("Error opening file");
        return NULL;
    }

    // Seek to the end of the file to determine its size
    if (fseek(file, 0, SEEK_END) != 0) {
        perror("Error seeking in file");
        fclose(file);
        return NULL;
    }

    size_t file_size = ftell(file);
    if (file_size == (size_t)-1) {
        perror("Error getting the file size");
        fclose(file);
        return NULL;
    }

    // Go back to the start of the file
    if (fseek(file, 0, SEEK_SET) != 0) {
        perror("Error seeking in file");
        fclose(file);
        return NULL;
    }

    // Allocate memory for the file contents
    uint8_t* buffer = (uint8_t*)malloc(file_size);
    if (buffer == NULL) {
        perror("Error allocating memory for file");
        fclose(file);
        return NULL;
    }

    // Read the file into the buffer
    if (fread(buffer, 1, file_size, file) != file_size) {
        perror("Error reading file");
        fclose(file);
        free(buffer);
        return NULL;
    }

    fclose(file);
    *size = file_size;

    return buffer;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Adf::create_for_exefile(const char* name) {
    size_t size;

    uint8_t* buffer = read_file_to_memory(name, &size);

    if (!buffer) {
        printf("Error reading file\n");
        exit(1);
    }

    adfEnvInitDefault();

    // Create a new ADF file and format it
    struct AdfDevice* device = adfCreateDumpDevice("dummy.adf", 80, 2, 11);
    if (!device) {
        printf("Error creating ADF file\n");
        exit(1);
    }

    if (adfCreateFlop(device, name, 0) != RC_OK) {
        printf("Error creating ADF floppy\n");
        exit(1);
    }

    // Mount the volume
    struct AdfVolume* volume = adfMount(device, 0, false);
    if (!volume) {
        printf("Error mounting ADF volume\n");
        exit(1);
    }

    if (adfInstallBootBlock(volume, s_bootblock) != RC_OK) {
        printf("Error installing boot block\n");
        exit(1);
    }

    printf("Creating file: %s %d\n", name, (int)size);

    struct AdfFile* file = adfFileOpen(volume, name, ADF_FILE_MODE_WRITE);
    if (!file) {
        printf("Error creating file: %s\n", name);
        exit(1);
    }

    adfFileWrite(file, size, buffer);
    adfFileClose(file);

    // Create the S directory
    if (adfCreateDir(volume, volume->curDirPtr, "T") != RC_OK) {
        printf("Error creating T directory\n");
        exit(1);
    }

    // Create the S directory
    if (adfCreateDir(volume, volume->curDirPtr, "S") != RC_OK) {
        printf("Error creating S directory\n");
        exit(1);
    }

    adfChangeDir(volume, "S");

    // Create the Startup-Sequence file and write commands to it
    file = adfFileOpen(volume, "Startup-Sequence", ADF_FILE_MODE_WRITE);
    if (file) {
        adfFileWrite(file, strlen(name), (unsigned char*)name);
        adfFileClose(file);
    } else {
        printf("Error creating Startup-Sequence file\n");
        exit(1);
    }

    adfUnMount(volume);
    adfUnMountDev(device);

    adfEnvCleanUp();
}

